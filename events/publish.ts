discord.on('MESSAGE_CREATE', async (message) => {
  let channel_id = ' ';
  let news_channel = await discord.getGuildNewsChannel(channel_id);
  if (message.channel.id == channel_id) {
    await news_channel.publishMessage(message.id);
  }
});

